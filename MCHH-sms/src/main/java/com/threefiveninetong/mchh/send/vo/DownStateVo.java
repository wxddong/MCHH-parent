package com.threefiveninetong.mchh.send.vo;

public class DownStateVo {

	public String code;//返回码
	
	public String msg;//返回消息
	
	public String phone;//手机号
	
	public String msgDownId;//下行id
	
	public String downTime;//下发时间

	
	public String getCode() {
		return code;
	}




	public void setCode(String code) {
		this.code = code;
	}




	public String getMsg() {
		return msg;
	}




	public void setMsg(String msg) {
		this.msg = msg;
	}




	public String getPhone() {
		return phone;
	}




	public void setPhone(String phone) {
		this.phone = phone;
	}




	public String getMsgDownId() {
		return msgDownId;
	}




	public void setMsgDownId(String msgDownId) {
		this.msgDownId = msgDownId;
	}




	public String getDownTime() {
		return downTime;
	}




	public void setDownTime(String downTime) {
		this.downTime = downTime;
	}




	
	
	
	
}
